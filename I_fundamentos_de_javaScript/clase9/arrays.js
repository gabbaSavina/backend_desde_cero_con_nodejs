/*ARRAYS: nos permiten generar una coleccion de datos ordenados,
sin importar el tipo de dato*/

let pelisFavoritas = ['Star Wars', 'Kill Bill', 'Alien'];
//                    cada dato de un array ocupa una posición numerada conocida
//                    como ÍNDICE. la 1° posición de un array es 0

pelisFavoritas[2]; //accedemos a la pélicula Aline, el índice 2 del array

/*Cantidad de elementos
se puede saber el números de elementos 
de un array usando la propiedad/método LENGTH*/

pelisFavoritas.length;
// devuelte 3, el número de elementos del array