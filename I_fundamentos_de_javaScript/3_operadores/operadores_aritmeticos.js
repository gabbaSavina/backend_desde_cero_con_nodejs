/* los operadores son símbolos que se utilizan para realizar operaciones en variables y valores.*/

/*OPERADORES ARITMETICOS*/

/*   + (Adición): Suma dos números    */

let a = 5 + 7;

/*    - (Sustracción): Resta el segundo número del primero       */

let b = 5 - 3;

/*          * (Multiplicación): Multiplica dos números           */

let c = 4 * 8;

/*    / (División): Divide el primer número por el segundo       */

let d = 12 / 4;

